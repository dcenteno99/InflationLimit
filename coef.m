function c = coef(a,b,c,a2,b2,c2,p,k)
E1=correlator1(p,k);
E2=correlator2(p,k);
c = -1-(a+b+c+a2+b2+c2)*E1-(a*b2+a2*b+b*c2+b2*c+a*c+a2*c2+a*a2+b*b2+c*c2)*E1*E1...
    -(a*b2*c+a2*b*c2)*E1*E1*E1-(a*b+a2*b2+b*c+b2*c2+c*a2+c2*a)*E2...
    -(a*a2*(b+b2+c+c2)+b*b2*(a+a2+c+c2)+c*c2*(a+a2+b+b2))*E1*E2...
    -(a*a2*b*b2+b*b2*c*c2+a*a2*c*c2)*E2*E2;
end